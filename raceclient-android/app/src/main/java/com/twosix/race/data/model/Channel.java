package com.twosix.race.data.model;

/**
 * Internal model of a channel that can be enabled or disabled by the user.
 *
 * <p>This differs from a JChannelProperties in that the enabled state is stored as a boolean rather
 * than the channel status enum.
 */
public class Channel {

    private String channelId;
    private boolean enabled;

    public String getChannelId() {
        return channelId;
    }

    public void setChannelId(String channelId) {
        this.channelId = channelId;
    }

    public boolean isEnabled() {
        return enabled;
    }

    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }

    @Override
    public String toString() {
        return channelId + " (enabled=" + enabled + ")";
    }
}
